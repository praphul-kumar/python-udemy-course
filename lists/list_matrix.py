# ------------- Matrix --------------

"""
    Matrix is a represantation of a Multi-Dimensional List
"""

matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

# We can access elements of matrix by following the below pattern

print(matrix[0][1]) # 2
print(matrix[1][0]) # 4
print(matrix[2][2]) # 9